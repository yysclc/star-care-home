export function getWeeklyFixedDialog(week, key) {
  const weekNumber = Number(week);
  if (weekNumber === 1) {
    const week1 = {
      beforeFixed: DIALOGS_FIXED.day1.office.todayFlowIntro,
      afterFixedActions: DIALOGS_FIXED.day1.afterFixedActions.childrenMoment,
      beforeFree: DIALOGS_FIXED.day1.office.freePhaseIntro,
    };
    return week1[key] ?? [`[第1周${key}占位文本 - 待写]`];
  }

  return DIALOGS_FIXED.weekly?.[weekNumber]?.[key]
    ?? [`[第${weekNumber}周${key}占位文本 - 待写]`];
}

