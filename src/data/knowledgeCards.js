const GLOBAL_KNOWLEDGE_CARDS_KEY = 'asd_care_global_knowledge_cards_v1';

function readGlobalKnowledgeCardsRaw() {
  try {
    const raw = localStorage.getItem(GLOBAL_KNOWLEDGE_CARDS_KEY);
    if (!raw) return { unlocked: {} };
    const parsed = JSON.parse(raw);
    return {
      unlocked: parsed?.unlocked && typeof parsed.unlocked === 'object' ? parsed.unlocked : {},
    };
  } catch {
    return { unlocked: {} };
  }
}

function writeGlobalKnowledgeCardsRaw(state) {
  try {
    localStorage.setItem(GLOBAL_KNOWLEDGE_CARDS_KEY, JSON.stringify({
      version: 1,
      updatedAt: Date.now(),
      unlocked: state?.unlocked ?? {},
    }));
    return true;
  } catch {
    return false;
  }
}

export function getKnowledgeCard(week, phase) {
  var w = Number(week);
  var card = KNOWLEDGE_CARDS[w] ? KNOWLEDGE_CARDS[w][phase] : null;
  if (!card) return null;
  return {
    ...card,
    week: w,
    phase,
  };
}

export function getAllKnowledgeCards() {
  return Object.entries(KNOWLEDGE_CARDS).flatMap(([week, phases]) => (
    Object.entries(phases).map(([phase, card]) => ({
      ...card,
      week: Number(week),
      phase,
    }))
  ));
}

export function getKnowledgeCardsBySection(sectionId) {
  return getAllKnowledgeCards().filter((card) => card.sectionId === sectionId);
}

export function unlockGlobalKnowledgeCard(cardId) {
  if (!cardId) return false;
  const exists = getAllKnowledgeCards().some((card) => card.id === cardId);
  if (!exists) return false;

  const global = readGlobalKnowledgeCardsRaw();
  if (global.unlocked[cardId]) return false;

  global.unlocked[cardId] = { obtainedAt: Date.now() };
  writeGlobalKnowledgeCardsRaw(global);
  return true;
}

export function isKnowledgeCardUnlocked(cardId, gs = null) {
  if (gs?.knowledgeCards?.unlocked?.[cardId]) return true;
  return Boolean(readGlobalKnowledgeCardsRaw().unlocked?.[cardId]);
}
