const CHAR_MAP = new Map([...SIMPLIFIED].map((char, index) => [char, TRADITIONAL[index] ?? char]));

export function getLanguage() {
  try {
    return localStorage.getItem(LANGUAGE_KEY) === LANGUAGES.zhHant ? LANGUAGES.zhHant : LANGUAGES.zhHans;
  } catch {
    return LANGUAGES.zhHans;
  }
}

export function setLanguage(language) {
  const next = language === LANGUAGES.zhHant ? LANGUAGES.zhHant : LANGUAGES.zhHans;
  try {
    localStorage.setItem(LANGUAGE_KEY, next);
  } catch {}
  document.documentElement.lang = next === LANGUAGES.zhHant ? 'zh-Hant' : 'zh-CN';
  window.dispatchEvent(new CustomEvent('starcare:language-change', { detail: { language: next } }));
}

export function toggleLanguage() {
  const next = getLanguage() === LANGUAGES.zhHant ? LANGUAGES.zhHans : LANGUAGES.zhHant;
  setLanguage(next);
  return next;
}

export function getLanguageLabel() {
  return getLanguage() === LANGUAGES.zhHant ? '繁體中文' : '简体中文';
}

export function toTraditional(input) {
  let text = String(input ?? '');
  PHRASE_MAP.forEach((replacement, source) => {
    text = text.split(source).join(replacement);
  });
  return [...text].map((char) => CHAR_MAP.get(char) ?? char).join('');
}

export function localizeText(input) {
  if (Array.isArray(input)) return input.map((item) => localizeText(item));
  if (typeof input !== 'string') return input;
  return getLanguage() === LANGUAGES.zhHant ? toTraditional(input) : input;
}

export function installPhaserTextLocalization(Phaser) {
  const proto = Phaser?.GameObjects?.Text?.prototype;
  if (!proto || proto.__starcareLanguagePatched) return;

  const originalSetText = proto.setText;
  proto.setText = function setLocalizedText(value) {
    this.__starcareRawText = value;
    return originalSetText.call(this, localizeText(value));
  };

  proto.refreshLanguage = function refreshLanguage() {
    if (Object.prototype.hasOwnProperty.call(this, '__starcareRawText')) {
      originalSetText.call(this, localizeText(this.__starcareRawText));
    }
    return this;
  };

  proto.__starcareLanguagePatched = true;
}

export function refreshSceneLanguage(scene) {
  scene?.children?.list?.forEach((child) => {
    child?.refreshLanguage?.();
  });
}
